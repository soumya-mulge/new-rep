package com.engine;

public class Engine
{
private String engno;
private int ccap;
public Engine(String engno, int ccap)
{
	this.engno = engno;
	this.ccap = ccap;
}
public String getEngno() {
	return engno;
}
public void setEngno(String engno) {
	this.engno = engno;
}
public int getCcap() {
	return ccap;
}
public void setCcap(int ccap) {
	this.ccap = ccap;
}

}




